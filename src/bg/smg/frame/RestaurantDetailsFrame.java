    public RestaurantDetailsFrame(Restaurant restaurant) throws SQLException {
        this.restaurant = restaurant;
        userService = new UserService();
        initComponents();
        jLabelName.setText(this.restaurant.getName());
        jLabelAddress.setText(this.restaurant.getAddress());
        
        String imagePath = "/resources/restaurant_images/" + restaurant.getImage();
        URL imageUrl = getClass().getResource(imagePath);
        ImageIcon imageIcon = new ImageIcon(imageUrl);
        SwingUtilities.invokeLater(() -> {
            Image image = imageIcon.getImage().getScaledInstance(jLabelImage.getWidth(), jLabelImage.getHeight(), Image.SCALE_SMOOTH);
            imageIcon.setImage(image);
            jLabelImage.setIcon(imageIcon);
        });

        User owner = userService.getUserById(this.restaurant.getOwnerId());
        jLabelOwner.setText(jLabelOwner.getText() + owner.getName());
        jLabelRating.setText(this.restaurant.getRating() + jLabelRating.getText());
        jLabelPricePoint.setText(this.restaurant.getPricePoint().toString());
        
    }

